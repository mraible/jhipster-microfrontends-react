import { element, by, ElementFinder } from 'protractor';

export default class BlogUpdatePage {
  pageTitle: ElementFinder = element(by.id('gatewayApp.blogBlog.home.createOrEditLabel'));
  saveButton: ElementFinder = element(by.id('save-entity'));
  cancelButton: ElementFinder = element(by.id('cancel-save'));
  nameInput: ElementFinder = element(by.css('input#blog-name'));
  handleInput: ElementFinder = element(by.css('input#blog-handle'));
  userSelect: ElementFinder = element(by.css('select#blog-user'));

  getPageTitle() {
    return this.pageTitle;
  }

  async setNameInput(name) {
    await this.nameInput.sendKeys(name);
  }

  async getNameInput() {
    return this.nameInput.getAttribute('value');
  }

  async setHandleInput(handle) {
    await this.handleInput.sendKeys(handle);
  }

  async getHandleInput() {
    return this.handleInput.getAttribute('value');
  }

  async userSelectLastOption() {
    await this.userSelect.all(by.tagName('option')).last().click();
  }

  async userSelectOption(option) {
    await this.userSelect.sendKeys(option);
  }

  getUserSelect() {
    return this.userSelect;
  }

  async getUserSelectedOption() {
    return this.userSelect.element(by.css('option:checked')).getText();
  }

  async save() {
    await this.saveButton.click();
  }

  async cancel() {
    await this.cancelButton.click();
  }

  getSaveButton() {
    return this.saveButton;
  }
}
